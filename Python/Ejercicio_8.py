# Operadores aritméticos #

print("\nPrograma que manipula datos numéricos\n")

x = 7
y = 2

print("Suma", x+y) # Suma #
print("Resta", x-y) # Resta #
print("Multiplicación", x*y) # Producto #
print("División", x/y) # División #
print("Resto", x%y) # Módulo #
print("Cociente", x//y) # Cociente #
print("Potencia", x**y) # Potencia #

