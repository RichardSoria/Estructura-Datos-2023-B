print("Programa que pida el día de la semana (del 1 al 7) y escriba el día correspondiente")

dia = int(input("Ingrese un número del 1 a 7 para mostrar el día que eligio:\n"))

if dia==1:
    print("El día de la semana es Lunes")
elif dia==2:
    print("El día de la semana es Martes")
elif dia==3:
    print("El día de la semana es Miércoles")
elif dia==4:
    print("El día de la semana es Jueves")
elif dia==5:
    print("El día de la semana es Viernes")
elif dia==6:
    print("El día de la semana es Sábado")
elif dia==7:
    print("El día de la semana es Domingo")
else:
    print(dia,"No es un dígito válido")

